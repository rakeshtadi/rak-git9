//ARITHMETIC OPERATORS
let a = 10;
let b = 5;
console.log("a = ",a, "b = ", b);
console.log("a + b = ", a+b);
console.log("a - b = ", a-b);
console.log("a * b = ", a*b);
console.log("a / b = ", a/b);
console.log("a ** b = ", a**b);
console.log("a % b = ", a%b);

//Unary operators
a++;
console.log("a = ", a); // Post increment
--a;
console.log("--a =", --a); //Pre decrement


//Assignment operator
let a1 = 11;

a1 += 5;
console.log("a1 = ", a1);

a1 -= 5;
console.log("a1 =",a1);
a1 *= 5;
console.log("a1 = ", a1);
a1 /= 5;
console.log("a1 = ", a1);
a1 %= 5;
console.log("a1 = ", a1);
a1 **= 5;
